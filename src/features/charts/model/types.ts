/**
 * Точка временного ряда метрик одного узла на момент модельного времени t.
 *  - cumulative — накопленный счётчик заявок (источник: создано, операция:
 *    обработано, сток: получено)
 *  - queue      — текущая длина очереди (для операций; для источников/стоков = 0)
 *  - utilization — текущая загрузка ρ (для операций; иначе 0)
 *
 * Темп (rate) не хранится отдельно — вычисляется на лету как производная
 * cumulative по времени.
 */
export interface ChartSample {
  t: number
  cumulative: number
  queue: number
  utilization: number
}
